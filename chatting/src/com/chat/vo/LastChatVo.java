package com.chat.vo;

public class LastChatVo {

	private int lastChatNum;
	private int userNum;
	private int chatNum;
	private int chatroomNum;
	
	public int getLastChatNum() {
		return lastChatNum;
	}
	public void setLastChatNum(int lastChatNum) {
		this.lastChatNum = lastChatNum;
	}
	public int getUserNum() {
		return userNum;
	}
	public void setUserNum(int userNum) {
		this.userNum = userNum;
	}
	public int getChatNum() {
		return chatNum;
	}
	public void setChatNum(int chatNum) {
		this.chatNum = chatNum;
	}
	public int getChatroomNum() {
		return chatroomNum;
	}
	public void setChatroomNum(int chatroomNum) {
		this.chatroomNum = chatroomNum;
	}
	
}
